import (
	"github.com/spf13/cobra"
	"github.com/winkoz/plonk/internal"
	"github.com/winkoz/plonk/internal/config"
	"github.com/winkoz/plonk/internal/io/log"
	"github.com/winkoz/plonk/internal/management"
)

func addShowCommand(rootCmd *cobra.Command, runtimeCtx internal.RuntimeContext) {
	var showCmd = &cobra.Command{
		Use:     "show",
		Short:   "Gets the instances for a service via a configurations generated by plonk.",
		Run:     newShowCommandHandler(runtimeCtx),
		Example: "plonk show",
		Args:    cobra.MaximumNArgs(1),
	}

	rootCmd.AddCommand(showCmd)
}

func newShowCommandHandler(runtimeCtx internal.RuntimeContext) CobraHandler {
	return func(cmd *cobra.Command, args []string) {
		ctx, err := config.NewContextFromFile()
		if err != nil {
			log.Fatal(err)
		}

		env := defaultEnvironment
		if len(args) == 1 {
			env = args[0]
		}

		ctx.RuntimeContext = runtimeCtx

		m := management.NewManager(ctx)
		m.GetPods(env)
	}
}
